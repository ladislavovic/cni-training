package com.cross_ni.training.springintroduction.common;

public class CommonBean {
	
	private String value;

	public CommonBean() {
	}

	public CommonBean(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
}
